    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        try
        {
            node=new Node();
            node.setTitle(txtTitle.getText());
            node.setAuthorName(txtAuthorName.getText());
            node.setAuthorSurname(txtSurname.getText());
            node.setIsbn(Integer.parseInt(txtIsbn.getText()));
            if (DbUtil.insertValuesToDB(node))
            {
                JOptionPane.showMessageDialog(null , "Added!");
            }
            else
            {
                JOptionPane.showMessageDialog(null , "NOT INSERTED!");
            }

        }
        catch (NumberFormatException e)
        {
            JOptionPane.showMessageDialog(null , e.getMessage());
        } 
        catch (ClassNotFoundException ex) 
        {
            Logger.getLogger(frmMain.class.getName()).log(Level.SEVERE, null, ex);
        } 
        catch (SQLException ex) 
        {
            Logger.getLogger(frmMain.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnQuickSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnQuickSearchActionPerformed
        // TODO add your handling code here:

        Node nodeSearch=new Node();
        if (radionISBN.isSelected() )
        {
            nodeSearch.setIsbn(Integer.parseInt(txtSearch.getText()));
        }
        else
        {
            nodeSearch.setTitle(txtSearch.getText());
        } 
       tree=new BinaryTree();
        Node result=tree.SearchNode(nodeSearch);
            
        //Fill the data grid
        
        //Set columns & add them to table
        
        DefaultTableModel dTable=new DefaultTableModel();
        
        Object [] columnNames=new Object[4];
        columnNames[0]="ISBN";
        columnNames[1]="Title";
        columnNames[2]="Author Name";
        columnNames[3]="Author Surname";
        
        dTable.setColumnIdentifiers(columnNames);
        
        //Set column values & add them to tables
        
        Object [] values =new Object[4];
        values[0]=Integer.toString(result.getIsbn());
        values[1]=result.getTitle();
        values[2]=result.getAuthorName();
        values[3]=result.getAuthorSurname();
        
        dTable.addRow(values);
        
        tableResult.setModel(dTable);
        

    }//GEN-LAST:event_btnQuickSearchActionPerformed

