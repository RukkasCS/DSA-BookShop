    private void btnAdvancedSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdvancedSearchActionPerformed
        // TODO add your handling code here:
        
        try 
        {
            //Data table column names
            
            DefaultTableModel dTable=null;
            dTable=new DefaultTableModel();
            Object [] columnNames=new Object[4];
            columnNames[0]="ISBN";
            columnNames[1]="Title";
            columnNames[2]="Author Name";
            columnNames[3]="Author Surname";
            dTable.setColumnIdentifiers(columnNames);
            
            Object [] values =new Object[4];
             
            Node node=new Node();
            node.setTitle(txtAdvancedSearch.getText());
            
            for (Node resultNode : DbUtil.getSimilarNodes(node)) 
            {
                values[0]=resultNode.getIsbn();
                values[1]=resultNode.getTitle();
                values[2]=resultNode.getAuthorName();
                values[3]=resultNode.getAuthorSurname();  
                dTable.addRow(values);
            }
           
          
            
            tableAdvancedResult.setModel(dTable);
        } 
        catch (Exception e) 
        {
            JOptionPane.showConfirmDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnAdvancedSearchActionPerformed

